for i in range(10):         #if not provided the start value its auto consider as 0
    print("i is now {}".format(i))

for i in range(0, 10, 2):          #to provide a step value we need to include the start value
    print("i is now {}".format(i))

for i in range(10, 0, -2):          #stepping backward
    print("i is now {}".format(i))